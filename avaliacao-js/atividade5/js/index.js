// 5) De exemplo de os tipos de funções:

// . Função Nomeada;
function funcaoNomeada(){
  alert("nomeada");
}

// . Função Nomeada com argumento(parametro) e retorno;
function funcaoArgumento(argumento){
  alert("argumento");
}

// . Função com Construtor
function funcaoConstrutor(){
  alert("construtor");
}

// . Função Literal (variável)
function funcaoLiteral(){
  alert("literal");
}

// . Função de Flecha(Arrow functions)
function funcaoFlecha(){
  alert("flecha");
}
